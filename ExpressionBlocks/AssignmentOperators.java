package ExpressionBlocks;

public class AssignmentOperators {
    
    public static void main(String[] args) {

        int age;
        age = 5;

        int a = 20;
        int val = age;
        System.out.println(val);
        val += a; //val = val + a => 5 + 20
        System.out.println(val);

    }

  
    
}
